/// json_to_dart https://javiercbk.github.io/json_to_dart/
class PredictionAutogenerated {
  double prediction;
  double ci68;
  double ci95;
  List<double> scaleBounds;
  double targetMean;

  PredictionAutogenerated(
      {this.prediction,
      this.ci68,
      this.ci95,
      this.scaleBounds,
      this.targetMean});

  PredictionAutogenerated.fromJson(Map<String, dynamic> json) {
    prediction = json['prediction'];
    ci68 = json['ci68'];
    ci95 = json['ci95'];
    scaleBounds = json['scale_bounds'].cast<double>();
    targetMean = json['target_mean'];
    print(
        'prediction,ci68,ci95,scaleBounds,targetMean below:\n$prediction,$ci68,$ci95,$scaleBounds,$targetMean');
  }
}

class PredictionRoute extends StatefulWidget {
  @override
  State<PredictionRoute> createState() => _PredictionRouteState();
}

class _PredictionRouteState extends State<PredictionRoute> {
  @override
  Widget build(BuildContext context) {
    /// read files
    Future<PredictionAutogenerated> _readPhonePredictionIOFiles() async {
      String data = await DefaultAssetBundle.of(context)
          .loadString("assets/tmp_phone_io/prediction.json");
      final jsonResult = jsonDecode(data); //latest Dart
      PredictionAutogenerated predictions =
          PredictionAutogenerated.fromJson(jsonResult);
      return predictions;
    }

    Future<List<List<dynamic>>> _readPhoneFeatureDataIOFiles() async {
      final String data = await DefaultAssetBundle.of(context)
          .loadString("assets/tmp_phone_io/gantt_chart.csv");
      final List<List<dynamic>> featureDataListList =
          const CsvToListConverter().convert(data);
      return featureDataListList;
    }

    Future<List<List<dynamic>>> _readPhoneWVCIOFiles() async {
      final String data = await DefaultAssetBundle.of(context)
          .loadString("assets/tmp_phone_io/wvc_chart.csv");
      final List<List<dynamic>> featureDataListList =
          const CsvToListConverter().convert(data);
      return featureDataListList;
    }

    BoxDecoration _predictionBoxDecoration() {
      return BoxDecoration(
        border: Border.all(width: 1.5),
        borderRadius: BorderRadius.all(Radius.circular(1.0)),
        gradient: const LinearGradient(
          begin: Alignment.centerLeft,
          end: Alignment(0.8, 0.0),
          // 10% of the width, so there are ten blinds.
          colors: const <Color>[
            const Color(0xFFE15554),
            const Color(0xFFffd97d),
            const Color(0xFF7DD181)
          ], // red to yellow
        ),
      );
    }

    Widget _scaledBar(
        start, end, scaleBounds, color, height, textBeforeBar, arrow) {
      const int accuracyFactor = 1000;
      if (start > end) {
        int tmp = start;
        start = end;
        end = tmp;
      }
      final int startCorrected =
          ((start - scaleBounds[0]) * accuracyFactor).toInt();
      final int endCorrected =
          ((end - scaleBounds[0]) * accuracyFactor).toInt();
      return Row(children: [
        /// left empty space
        Expanded(
            flex: (startCorrected),
            child: Container(
              alignment: Alignment.centerLeft,
              height: height,
              child: Row(
                children: [
                  SizedBox(width: 2),
                  Text(textBeforeBar),
                ],
              ),
            )),

        /// prediction
        Expanded(
            flex: (endCorrected - startCorrected),
            child: Container(
              height: height,
              color: color,
              child: arrow == true
                  ? color == const Color(0xFF7DD181) //const Color(0xFF9dbc95)
                      ? FittedBox(child: Icon(Icons.arrow_forward_sharp))
                      : color ==
                              const Color(0xFFE15554) //const Color(0xFF855e78)
                          ? FittedBox(child: Icon(Icons.arrow_back_sharp))
                          : Container()
                  : Container(),
            )),

        /// right empty space
        Expanded(
          flex: (((scaleBounds[1] - scaleBounds[0] + 1) * accuracyFactor)
                  .toInt() -
              accuracyFactor -
              endCorrected),
          child: Container(height: height),
        ),
      ]);
    }

    Widget _gradientColorScale(predictions) {
      return Stack(
        children: <Widget>[
          Image(
            image: AssetImage('assets/tmp_phone_io/tokyo_crop.png'),
            height: 30,
          ),
          Container(
            height: 30.0,
            decoration: _predictionBoxDecoration(),
            child: FractionallySizedBox(
              widthFactor: 1,
              child: Column(
                  mainAxisAlignment: MainAxisAlignment.center,
                  children: [
                    _scaledBar(
                        predictions.prediction - 0.1,
                        predictions.prediction + 0.1,
                        predictions.scaleBounds,
                        Colors.black,
                        8.0,
                        '',
                        true),
                    _scaledBar(
                        predictions.prediction - predictions.ci68,
                        predictions.prediction + predictions.ci68,
                        predictions.scaleBounds,
                        Colors.black,
                        6.0,
                        '',
                        true),
                    _scaledBar(
                        predictions.prediction - predictions.ci95,
                        predictions.prediction + predictions.ci95,
                        predictions.scaleBounds,
                        Colors.black,
                        3.0,
                        '',
                        true),
                  ]),
            ),
          ),
        ],
      );
    }

    Widget _biDirectionalGanttChart(scaleBounds) {
      return FutureBuilder(
        future: _readPhoneFeatureDataIOFiles(),
        builder: (context, snapshot) {
          if (snapshot.connectionState == ConnectionState.done) {
            final List<List<dynamic>> featureEndStarts = snapshot.data;
            const double height = 16.0;

            List<Widget> _ganttChildren(featureEndStarts) {
              List<Widget> list = [];
              //i<5, pass your dynamic limit as per your requirment
              for (int i = 1; i < featureEndStarts.length; i++) {
                Color color =
                    const Color(0xFFE15554); //const Color(0xFF855e78);
                if ((featureEndStarts[i][3]) == 'True') {
                  color = const Color(0xFF7DD181); //const Color(0xFF9dbc95);
                }
                list.add(
                  _scaledBar(featureEndStarts[i][1], featureEndStarts[i][2],
                      scaleBounds, color, height, featureEndStarts[i][0], true),
                ); //add any Widget in place of Text("Index $i")
              }
              return list; // all widget added now retrun the list here
            }

            return Column(
              children: _ganttChildren(featureEndStarts),
            );
          } else {
            return Container(
              child: CircularProgressIndicator(),
            );
          }
        },
      );
    }

    Widget _scaleTile(num number) {
      return Column(
        children: [
          Container(color: Colors.black, height: 5, width: 1),
          Text('$number')
        ],
      );
    }

    _numericScale(scaleBounds) {
      var text = new List<int>.generate(scaleBounds[1].floor().toInt(), (i) {
        return i + 1;
      });
      return Container(
        child: FractionallySizedBox(
          widthFactor: 1,
          child: Row(
            mainAxisAlignment: MainAxisAlignment.spaceBetween,
            children: [for (var i in text) _scaleTile(i)],
          ),
        ),
      );
    }

    Widget _showGanttExplanation() {
      return TextButton(
        style: TextButton.styleFrom(
            padding: EdgeInsets.fromLTRB(5, 0, 5, 0),
            tapTargetSize: MaterialTapTargetSize.shrinkWrap),
        /* info note for correlation coefficient */
        // to reduce height of correlation info button
        child: Icon(Icons.info, color: Colors.grey),
        onPressed: () {
          showAlertDialog(
              'Explanation',
              'The top black bar shows your predicted mood\n'
                  'The other larger black bars show its 68% and 95% prediction '
                  'intervals.\n'
                  'The green and red bars show positive and negative '
                  'contributions of the prediction.\n'
                  'One contribution consists of today\'s value multiplied by a '
                  'coefficient obtained through multiple linear regression.',
              context);
        },
      );
    }

    Widget _showWVCExplanation(
        contributionColor, weightColor, valueTodayColor) {
      return TextButton(
        style: TextButton.styleFrom(
            padding: EdgeInsets.fromLTRB(5, 0, 5, 0),
            tapTargetSize: MaterialTapTargetSize.shrinkWrap),
        /* info note for correlation coefficient */
        // to reduce height of correlation info button
        child: Icon(Icons.info, color: Colors.grey),
        onPressed: () {
          AlertDialog alertDialog = AlertDialog(
            title: Text('Explanation'),
            content: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              mainAxisAlignment: MainAxisAlignment.start,
              children: [
                Row(children: [
                  Container(
                      color: contributionColor, height: 16.0, width: 16.0),
                  Text(' Contribution:'),
                ]),
                Text(                    'Contribution = Weight Â· Measurement.\n'
                    'Prediction = Sum(Contributions)\n    + Average Mood\n'
                    'In the example below it has a medium positive effect.\n'),
                Row(children: [
                  Container(color: weightColor, height: 16.0, width: 16.0),
                  Text(' Weight:'),
                ]),
                Text(
                    'Indicates how strong the effect is and if it\'s negative or positive.\n'
                        'In the example below Humidity has a strong negative weight.\n'),
                Row(children: [
                  Container(color: valueTodayColor, height: 16.0, width: 16.0),
                  Text(' Today\'s measurement:'),
                ]),
                Text('Indicates if today\'s humidity measurement is below or above average.\n'
                    'In the example Humidity is a little below average.\n'),
                Text('Example Humidity:'),
                _scaledBar(5, 6.7, [1,9], contributionColor,
                    16.0, '', false),
                _scaledBar(1.5, 5, [1,9], weightColor,
                    16.0, '', false),
                _scaledBar(4.5, 5, [1,9], valueTodayColor,
                    16.0, '', false),

                Text('\nAbout the model:\n'
                    'Multiple linear regression with lasso variable selection and regularization.'),
              ],
            ),
          );
          showDialog(context: context, builder: (_) => alertDialog);
        },
      );
    }

    Widget _wvcChart() {
      /// header: [0]featureName [1]contribution	[2]weight
      /// [3]value_today_not_normalized	[4]value_today_normalized
      /// [5]extrema[max,min]
      const Color contributionColor = const Color(0xFF946fc4);
      const Color weightColor = const Color(0xFF00b5b2); //Colors.teal;
      const Color valueTodayColor = const Color(0xFF5dddd7);
      return Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          SizedBox(height: 8),
          Row(children: [
            Container(color: contributionColor, height: 16.0, width: 16.0),
            Text(' Contribution  '),
            Container(color: weightColor, height: 16.0, width: 16.0),
            Text(' Weight  '),
            Container(color: valueTodayColor, height: 16.0, width: 16.0),
            Text(' Today\'s measurement  '),
          ]),
          SizedBox(height: 6),
          FutureBuilder(
            future: _readPhoneWVCIOFiles(),
            builder: (context, snapshot) {
              if (snapshot.connectionState == ConnectionState.done) {
                final List<List<dynamic>> featureData = snapshot.data;
                const double height = 16.0;
                List<double> scaleBounds = [
                  featureData[2][5],
                  featureData[1][5]
                ];

                List<Widget> _ganttChildren(featureData) {
                  List<Widget> list = [];
                  for (int i = 1; i < featureData.length; i++) {
                    list.add(Text('${featureData[i][0]}:'));
                    list.add(
                      _scaledBar(0, featureData[i][1], scaleBounds,
                          contributionColor, height, '', false), //
                    );
                    list.add(
                      _scaledBar(0, featureData[i][2], scaleBounds, weightColor,
                          height, '', false), //featureData[i][0]
                    );
                    list.add(
                      _scaledBar(
                          0,
                          featureData[i][4],
                          scaleBounds,
                          valueTodayColor,
                          height,
                          '',
                          false), //featureData[i][0]
                    );
                    list.add(
                      SizedBox(
                        height: 10,
                      ),
                    );
                  }
                  return list;
                }

                return Column(
                  children: _ganttChildren(featureData),
                  crossAxisAlignment: CrossAxisAlignment.start,
                );
              } else {
                return Container(
                  child: CircularProgressIndicator(),
                );
              }
            },
          ),
          _showWVCExplanation(contributionColor, weightColor, valueTodayColor),
        ],
      );
    }

    const bool _wvcChartEnabled = true;
    const bool _ganttChartEnabled = true;
    return Container(
      margin: const EdgeInsets.all(8),
      child: FutureBuilder(
          future: _readPhonePredictionIOFiles(),
          builder: (context, snapshot) {
            if (snapshot.connectionState == ConnectionState.done) {
              print('snapshot.data: ${snapshot.data.prediction}');
              return SingleChildScrollView(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    Container(
                      margin: EdgeInsets.fromLTRB(0, 0, 0, 5),
                      // padding: const EdgeInsets.fromLTRB(1, 8, 8, 8),
                      child: Text(
                        'Mood prediction for today with prediction interval:',
                        style: TextStyle(
                            fontSize: 15.5, fontWeight: FontWeight.w500),
                      ),
                    ),
                    _ganttChartEnabled == true
                        ? _biDirectionalGanttChart(snapshot.data.scaleBounds)
                        : Container(),
                    SizedBox(height: 3),
                    _gradientColorScale(snapshot.data),
                    _numericScale(snapshot.data.scaleBounds),
                    _ganttChartEnabled == true
                        ? _showGanttExplanation()
                        : Container(),
                    _wvcChartEnabled == true ? _wvcChart() : Container(),
                  ],
                ),
              );
            } else {
              return Container(
                child: CircularProgressIndicator(),
              );
            }
          }),
    );
  }
}
